/**
 * Генерирует вопросы по одной теме.
 */
export async function generateQuestions(params) {
  const {
    faculty,
    course,
    department,
    subject,
    topic,
    type,
    difficulty,
    count,
    language = 'ru',
  } = params;

  const langName = LANG_NAMES[language] || LANG_NAMES.ru;

  const anchor = SCORE_ANCHOR_HINT[difficulty] || SCORE_ANCHOR_HINT.medium;

  const user = `Составь ${count} тестовых вопросов.

Контекст: ${faculty}, ${course}, ${department}, ${subject}
Тема: ${topic}
Тип: ${TYPE_LABEL[type]}
Метка сложности блока (из сетки тем): ${DIFFICULTY_LABEL[difficulty]} — это требуемая «корзина» блока, не число.
${anchor}
Язык ВСЕХ текстов (вопросы, варианты, explanation): ${langName}

Формат ответа (строгий JSON):
{
  "questions": [
    {
      "question": "текст вопроса",
      "options": { "A": "...", "B": "...", "C": "...", "D": "..." },
      "correct": "A",
      "explanation": "почему правильный ответ верен и почему каждый дистрактор неверен (2-4 предложения)",
      "difficulty_score": 30,
      "difficulty_note": "кратко, почему такая трудность (число 30 в примере не копируй — своё по якорю блока)"
    }
  ]
}`;

  // Динамический лимит токенов: ~300 токенов на вопрос + запас
  const maxTokens = Math.min(8000, Math.max(1500, count * 350 + 800));

  const result = await callJSON({
    model: modelFor('generator'),
    system: GENERATOR_SYSTEM,
    user,
    maxTokens,
  });

  const questions = Array.isArray(result.questions) ? result.questions : [];
  const bucketFallback = DIFFICULTY_BUCKET_DEFAULT[difficulty] ?? 50;
  return questions.map((q) => ({
    ...q,
    topic,
    type,
    difficulty,
    difficulty_score: normalizeDifficultyScore(q.difficulty_score, bucketFallback),
    difficulty_note: sanitizeDifficultyNote(q.difficulty_note),
  }));
}
