import { callJSON, modelFor } from './llm.js';
import { LANG_NAMES } from './questionFormat.js';
import {
  normalizeDifficultyScore,
  sanitizeDifficultyNote,
  DIFFICULTY_BUCKET_DEFAULT,
} from './difficultyScore.js';

const DIFFICULTY_LABEL = {
  easy: 'лёгкий',
  medium: 'средний',
  hard: 'сложный',
};

const TYPE_LABEL = {
  knowledge: 'на знание (факты, определения, классификации)',
  understanding: 'на понимание (клинические задачи, анализ, интерпретация)',
};

/** Якоря difficulty_score по метке блока (в user-промпт). */
const SCORE_ANCHOR_HINT = {
  easy:
    'Якорь difficulty_score для этого блока: в основном 5–35; типичный прямой факт из программы с очевидными дистракторами — часто 10–22. Поднимай выше только если дистракторы очень близки по смыслу к верному.',
  medium:
    'Якорь difficulty_score: в основном 28–55; чаще 35–48 для стандартного материала с правдоподобными ошибками.',
  hard:
    'Якорь difficulty_score: в основном 48–88; узкие детали, сильная близость дистракторов или многошаговый анализ — ближе к верхней части диапазона.',
};

/**
 * Статичный system-промпт — не зависит от контекста темы, поэтому кешируется провайдером.
 */
const GENERATOR_SYSTEM = `Ты — преподаватель медицинского ВУЗа, составляющий MCQ-тесты по стандартам NBME/UWSOM.

ГОМОГЕННОСТЬ (главное правило): все четыре варианта отвечают на ОДИН И ТОТ ЖЕ «тип запроса» в стволе и лежат на ОДНОЙ логической оси. Студент НЕ должен находить верный ответ только потому, что остальные варианты явно из другой системы органов, другой этиологии или другого класса факторов.

Как выбрать ось (обязательно проговори её про себя перед генерацией):
1) Прочитай ствол: что именно спрашивают — симптом, признак, диагноз, фактор риска, препарат, показатель анализа, шаг лечения и т.д.
2) Зафиксируй ОДНУ категорию для всех четырёх позиций. Примеры ОСЕЙ (не смешивать между собой):
   - «Симптомы/признаки болезни X» — тогда все 4 варианта это симптомы или типичные проявления в контексте X или узкого дифференциального ряда (например при рахите: костные деформации, рыхлость краёв твёрдого нёба, задержка окостенения, мышечная гипотония — а НЕ тахипноэ, «острая инфекция», анемия).
   - «Факторы риска одного класса» — например только наследственные/генетические предрасположенности; или только экологические триггеры; или только нарушения ухода за кожей — но НЕ смесь «наследственность + дефицит витамина D + загрязнение воздуха + белковая диета» для одного вопроса про атопический дерматит (это разные классы, верный угадывается без знаний).
   - «Препараты той же группы», «показатели одного типа», «меры одного уровня (первая линия / профилактика)» и т.п.

АНТИПРИМЕРЫ (так делать ЗАПРЕЩЕНО — это ломает гомогенность):
- Вопрос про наиболее характерный симптом при рахите, а в вариантах костная деформация рядом с тахипноэ, острой инфекцией и анемией.
- Вопрос про фактор риска атопического дерматита, а в вариантах наследственная предрасположенность рядом с загрязнением воздуха, дефицитом витамина D и белковой недостаточностью питания.

Если для темы трудно набрать 4 варианта на одной оси — сузь или переформулируй ствол (например явно: «какой из перечисленных скелетных проявлений…» или «какой из немодифицируемых факторов…»), чтобы ось стала честной.

Требования к каждому вопросу:
- 4 варианта (A, B, C, D), ровно один правильный
- Дистракторы клинически правдоподобны, а не абсурдны
- Все варианты близкой длины (±50%) и одной грамматической формы
- Запрещены: "всё перечисленное", "ни один", "A и B"
- Без слов-подсказок ("всегда", "никогда")
- Без повторения слов из ствола только в правильном ответе
- Правильный ответ распределяется равномерно по A/B/C/D
- Клиническая точность обязательна

Для типа "понимание" — клинические ситуации с данными обследования, требующие анализа. Для "знание" — прямые вопросы на факты и классификации.

ОЦЕНКА СЛОЖНОСТИ (для каждого вопроса обязательно):
- "difficulty_score" — целое 0–100: прогноз **насколько ТРУДНО** типичному студенту курса ответить правильно. ВАЖНО: **больше число = СЛОЖНЕЕ вопрос**; это НЕ «качество вопроса», НЕ «процент правильных» и НЕ оценка студента.
- "difficulty_note" — одно короткое предложение на языке вопроса: почему такая трудность.

Метка сложности БЛОКА в пользовательском запросе («лёгкий/средний/сложный») — это **задание кафедры на весь блок**; difficulty_score задаёт **уточнение внутри блока** и должен в **типичном случае** попадать в диапазон якоря для этой метки (см. ниже в запросе пользователя), но разные вопросы в одном JSON должны получать **разные** difficulty_score, если содержательная трудность реально отличается (не копируй одно число всем подряд).

Критерии difficulty_score (совокупно):
1) Когнитивная глубина: чистое воспроизведение факта (ниже) vs анализ, несколько шагов, интеграция данных (выше).
2) Отсеиваемость дистракторов: легко отбросить неверное без знаний — ниже; все варианты правдоподобны — выше.
3) Смысловая близость дистракторов к верному (при гомогенных MCQ).
4) Нетипичность факта/ситуации в теме.
5) Для кейсов: объём и неочевидность данных в стволе.

Интерпретация итога: 0–25 низкая трудность; 26–50 умеренная; 51–75 высокая; 76–100 очень высокая.

Возвращай строго валидный JSON без markdown и преамбул.`;

/**
 * Генерирует вопросы по одной теме.
 */
export async function generateQuestions(params) {
  const {
    faculty,
    course,
    department,
    subject,
    topic,
    type,
    difficulty,
    count,
    language = 'ru',
  } = params;

  const langName = LANG_NAMES[language] || LANG_NAMES.ru;

  const anchor = SCORE_ANCHOR_HINT[difficulty] || SCORE_ANCHOR_HINT.medium;

  const user = `Составь ${count} тестовых вопросов.

Контекст: ${faculty}, ${course}, ${department}, ${subject}
Тема: ${topic}
Тип: ${TYPE_LABEL[type]}
Метка сложности блока (из сетки тем): ${DIFFICULTY_LABEL[difficulty]} — это требуемая «корзина» блока, не число.
${anchor}
Язык ВСЕХ текстов (вопросы, варианты, explanation): ${langName}

Формат ответа (строгий JSON):
{
  "questions": [
    {
      "question": "текст вопроса",
      "options": { "A": "...", "B": "...", "C": "...", "D": "..." },
      "correct": "A",
      "explanation": "почему правильный ответ верен и почему каждый дистрактор неверен (2-4 предложения)",
      "difficulty_score": 30,
      "difficulty_note": "кратко, почему такая трудность (число 30 в примере не копируй — своё по якорю блока)"
    }
  ]
}`;

  // Динамический лимит токенов: ~300 токенов на вопрос + запас
  const maxTokens = Math.min(8000, Math.max(1500, count * 350 + 800));

  const result = await callJSON({
    model: modelFor('generator'),
    system: GENERATOR_SYSTEM,
    user,
    maxTokens,
  });

  const questions = Array.isArray(result.questions) ? result.questions : [];
  const bucketFallback = DIFFICULTY_BUCKET_DEFAULT[difficulty] ?? 50;
  return questions.map((q) => ({
    ...q,
    topic,
    type,
    difficulty,
    difficulty_score: normalizeDifficultyScore(q.difficulty_score, bucketFallback),
    difficulty_note: sanitizeDifficultyNote(q.difficulty_note),
  }));
}
